fun main() {
    println("Em Kotlin, crie uma array com nome de frutas e crie um laço que imprima seus valores.")

    // Array de frutas
    val fruits = arrayOf("Abacate", "Coco", "Jabuticaba", "Mamão", "Pera")

    // Laço 'for' para imprimir as frutas
    println("As frutas listadas são:")
    for (fruit in fruits) {
        println(fruit)
    }
}